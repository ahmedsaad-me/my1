const body = document.body;
const buttons = document.querySelectorAll('.lang-btn');
const translatable = document.querySelectorAll('[data-i18n]');
const menuBtn = document.getElementById('menuBtn');
const navMenu = document.getElementById('navMenu');

function setLanguage(lang){
  const dict = translations[lang];
  if(!dict) return;
  translatable.forEach(el => {
    const key = el.dataset.i18n;
    if(dict[key]) el.textContent = dict[key];
  });
  buttons.forEach(btn => btn.classList.toggle('active', btn.dataset.lang === lang));
  body.classList.toggle('lang-ar', lang === 'ar');
  document.documentElement.lang = lang;
  localStorage.setItem('portfolio-lang', lang);
}

buttons.forEach(btn => btn.addEventListener('click', () => setLanguage(btn.dataset.lang)));
setLanguage(localStorage.getItem('portfolio-lang') || 'en');

const observer = new IntersectionObserver((entries) => {
  entries.forEach(entry => {
    if(entry.isIntersecting){ entry.target.classList.add('visible'); }
  });
},{threshold:.12});

document.querySelectorAll('.reveal').forEach(el => observer.observe(el));

menuBtn?.addEventListener('click', ()=> navMenu.classList.toggle('open'));
document.querySelectorAll('.nav a').forEach(link => link.addEventListener('click', ()=> navMenu.classList.remove('open')));

const sections = [...document.querySelectorAll('main section[id]')];
const navLinks = [...document.querySelectorAll('.nav a')];
window.addEventListener('scroll', () => {
  const scrollY = window.pageYOffset;
  sections.forEach(sec => {
    const top = sec.offsetTop - 120;
    const height = sec.offsetHeight;
    if(scrollY >= top && scrollY < top + height){
      navLinks.forEach(link => link.classList.toggle('active', link.getAttribute('href') === `#${sec.id}`));
    }
  });
});

const tiltCards = document.querySelectorAll('.tilt-card');
tiltCards.forEach(card => {
  card.addEventListener('mousemove', (e) => {
    if(window.innerWidth < 900) return;
    const rect = card.getBoundingClientRect();
    const x = e.clientX - rect.left;
    const y = e.clientY - rect.top;
    const rx = ((y / rect.height) - 0.5) * -8;
    const ry = ((x / rect.width) - 0.5) * 10;
    card.style.transform = `perspective(1000px) rotateX(${rx}deg) rotateY(${ry}deg) translateY(-4px)`;
  });
  card.addEventListener('mouseleave', () => {
    card.style.transform = 'perspective(1000px) rotateX(0deg) rotateY(0deg) translateY(0)';
  });
});
